package model;


public class Estado {
    private String uf;
    private double valorDividas;
    private int quantDevedores;
    private int quantDividas;
    private double valorAcoes;
    private int quantAcoes;

    public String getUf() {
        return uf;
    }

    public void setUf(String uf) {
        this.uf = uf;
    }

    public double getValorDividas() {
        return valorDividas;
    }

    public void setValorDividas(double valorDividas) {
        this.valorDividas = valorDividas;
    }

    public int getQuantDevedores() {
        return quantDevedores;
    }

    public void setQuantDevedores(int quantDevedores) {
        this.quantDevedores = quantDevedores;
    }

    public int getQuantDividas() {
        return quantDividas;
    }

    public void setQuantDividas(int quantDividas) {
        this.quantDividas = quantDividas;
    }

    public double getValorAcoes() {
        return valorAcoes;
    }

    public void setValorAcoes(double valorAcoes) {
        this.valorAcoes = valorAcoes;
    }

    public int getQuantAcoes() {
        return quantAcoes;
    }

    public void setQuantAcoes(int quantAcoes) {
        this.quantAcoes = quantAcoes;
    }
    
    
}
